DELIMITER //

DROP PROCEDURE IF EXISTS INSERT_EDIFICIO;
//

CREATE PROCEDURE INSERT_EDIFICIO(IN nombreEdificio VARCHAR(45))
BEGIN

INSERT INTO Edificio (nombre_Edificio) VALUES (nombreEdificio);

END
//



DROP PROCEDURE IF EXISTS GET_EDIFICIO;
//

CREATE PROCEDURE GET_EDIFICIO(IN NombreEdificio VARCHAR(45))

BEGIN

SELECT * FROM Edificio WHERE nombre_Edificio = NombreEdificio;

END
//

DELIMITER ;
